package user;

public class User {
	private String userID = null;
	private String userPassword = null;
	private String userName = null;
	private String userGender = null;
	private String userEmail = null;
	private String userJoin = null;
	private int userNumber;
	private String userSecurity = null;
	
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getUserPassword() {
		return userPassword;
	}
	public void setUserPassword(String userPassword) {
		this.userPassword = userPassword;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getUserGender() {
		return userGender;
	}
	public void setUserGender(String userGender) {
		this.userGender = userGender;
	}
	public String getUserEmail() {
		return userEmail;
	}
	public void setUserEmail(String userEmail) {
		this.userEmail = userEmail;
	}
	public String getUserJoin() {
		return userJoin;
	}
	public void setUserJoin(String userJoin) {
		this.userJoin = userJoin;
	}
	public int getUserNumber() {
		return userNumber;
	}
	public void setUserNumber(int userNumber) {
		this.userNumber = userNumber;
	}
	public String getUserSecurity() {
		return userSecurity;
	}
	public void setUserSecurity(String userSecurity) {
		this.userSecurity = userSecurity;
	}
	
}
